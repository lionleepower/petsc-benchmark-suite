#!/usr/bin/env python3
from __future__ import annotations

import os

os.environ.setdefault(
    "MPLCONFIGDIR",
    str(os.path.join(os.path.dirname(__file__), "..", ".matplotlib-cache")),
)

import matplotlib.pyplot as plt
import pandas as pd

from common import (
    SCALE_ORDER,
    THREAD_ORDER,
    aggregate_runtime_results,
    clean_runtime_results,
    ensure_output_dir,
    load_runtime_results,
    problem_size_label,
)


THREAD_MARKERS = {
    1: "o",
    2: "s",
    4: "^",
    8: "D",
}


def build_aggregated_data() -> pd.DataFrame:
    raw = load_runtime_results()
    clean = clean_runtime_results(raw)
    return aggregate_runtime_results(clean)


def plot_runtime_scaling(aggregated: pd.DataFrame, output_path, yscale: str) -> None:
    fig, axes = plt.subplots(2, 3, figsize=(15, 9), sharex=False, sharey=False)
    flat_axes = axes.ravel()

    for index, scale in enumerate(SCALE_ORDER):
        ax = flat_axes[index]
        scale_data = aggregated[aggregated["scale"].astype(str) == scale]
        if scale_data.empty:
            ax.set_visible(False)
            continue

        unknowns = int(scale_data["unknowns"].iloc[0])
        for threads in THREAD_ORDER:
            series = scale_data[scale_data["threads"].astype(int) == threads].sort_values("total_cores")
            if series.empty:
                continue
            ax.plot(
                series["total_cores"],
                series["median_runtime_seconds"],
                marker=THREAD_MARKERS[threads],
                linewidth=2,
                markersize=5,
                label=f"T = {threads}",
            )

        ax.set_title(problem_size_label(unknowns), fontweight="bold")
        ax.set_xscale("log", base=2)
        ax.set_yscale(yscale)
        ax.set_xticks([1, 2, 4, 8, 16, 32, 64, 128])
        ax.set_xticklabels(["1", "2", "4", "8", "16", "32", "64", "128"])
        ax.grid(True, which="major", alpha=0.35)
        ax.set_xlabel("Total cores")
        ax.set_ylabel("Runtime / solve time (s)")

    flat_axes[-1].axis("off")
    handles, labels = flat_axes[0].get_legend_handles_labels()
    flat_axes[-1].legend(handles, labels, title="Threads", loc="center", frameon=False)

    fig.suptitle("Runtime vs Total Cores", fontsize=18, fontweight="bold")
    fig.text(
        0.5,
        0.935,
        "Median runtime by problem size and OpenMP thread count",
        ha="center",
        color="dimgray",
    )
    fig.tight_layout(rect=[0, 0, 1, 0.92])
    fig.savefig(output_path, dpi=180)
    plt.close(fig)


def write_interpretation(aggregated: pd.DataFrame, output_path) -> None:
    lines = [
        "# Runtime Scaling Interpretation",
        "",
        "This file is generated by `scripts/04_plot_runtime_scaling.py`.",
        "",
        "The plot uses median runtime for each `(scale, total_cores, threads)` configuration.",
        "",
        "## Fastest Configuration by Problem Size",
        "",
        "| Problem size | Fastest total cores | Threads | Median runtime (s) | Speedup vs 1 core |",
        "| --- | ---: | ---: | ---: | ---: |",
    ]

    for scale in SCALE_ORDER:
        scale_data = aggregated[aggregated["scale"].astype(str) == scale]
        if scale_data.empty:
            continue
        best = scale_data.loc[scale_data["median_runtime_seconds"].idxmin()]
        baseline = scale_data[(scale_data["total_cores"] == 1) & (scale_data["threads"].astype(int) == 1)]
        speedup = float("nan")
        if not baseline.empty:
            speedup = baseline["median_runtime_seconds"].iloc[0] / best["median_runtime_seconds"]
        label = problem_size_label(int(best["unknowns"]))
        lines.append(
            f"| {label} | {int(best['total_cores'])} | {int(best['threads'])} | "
            f"{best['median_runtime_seconds']:.4g} | {speedup:.1f}x |"
        )

    lines.extend(
        [
            "",
            "## Questions To Answer From The Figure",
            "",
            "1. Runtime generally decreases as total cores increase, but not with ideal linear speedup.",
            "2. At the same total cores, compare vertical position: lower runtime is better.",
            "3. `T=1` is a strong high-core-count baseline; hybrid `T=2` or `T=4` can be competitive at intermediate core counts.",
            "4. `T=8` often has more overhead and is less competitive in these runs.",
            "5. The 1M case is more irregular, which is a useful sign that small problems can expose parallel overhead.",
            "6. Larger problems show clearer scaling trends, but still remain far from ideal 128x speedup.",
            "",
            "## How To Use This In The Report",
            "",
            "Use the log-y figure when you want to compare trends across the whole runtime range.",
            "Use the linear figure when you want to discuss absolute runtime differences.",
            "",
        ]
    )

    output_path.write_text("\n".join(lines))


def main() -> None:
    output_dir = ensure_output_dir()
    aggregated = build_aggregated_data()

    aggregate_path = output_dir / "runtime_scaling_aggregated_pandas.csv"
    linear_path = output_dir / "runtime_scaling_pandas_linear.png"
    logy_path = output_dir / "runtime_scaling_pandas_logy.png"
    interpretation_path = output_dir / "runtime_scaling_interpretation.md"

    aggregated.to_csv(aggregate_path, index=False)
    plot_runtime_scaling(aggregated, linear_path, yscale="linear")
    plot_runtime_scaling(aggregated, logy_path, yscale="log")
    write_interpretation(aggregated, interpretation_path)

    print(f"Wrote: {aggregate_path}")
    print(f"Wrote: {linear_path}")
    print(f"Wrote: {logy_path}")
    print(f"Wrote: {interpretation_path}")


if __name__ == "__main__":
    main()
