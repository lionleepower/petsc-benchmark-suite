#!/usr/bin/env python3
from __future__ import annotations

import math

import pandas as pd

from common import (
    SCALE_ORDER,
    aggregate_runtime_tables,
    clean_runtime_results_for_tables,
    ensure_output_dir,
    load_runtime_results,
)


def fmt_int(value: object) -> str:
    return f"{int(value):,}"


def fmt_float(value: object, digits: int = 2) -> str:
    number = float(value)
    if math.isnan(number):
        return "-"
    return f"{number:,.{digits}f}"


def fmt_percent(value: object) -> str:
    return f"{float(value) * 100:.1f}%"


def fmt_cu(value: object) -> str:
    return f"{float(value):.4f}"


def markdown_table(headers: list[str], rows: list[list[str]]) -> list[str]:
    lines = [
        "| " + " | ".join(headers) + " |",
        "| " + " | ".join("---" for _ in headers) + " |",
    ]
    lines.extend("| " + " | ".join(row) + " |" for row in rows)
    return lines


def write_markdown_tables(aggregated: pd.DataFrame, output_path) -> None:
    lines = [
        "# Runtime Scaling Tables: pandas Learning Version",
        "",
        "This file is generated by `scripts/05_generate_tables.py`.",
        "",
        "`speedup = baseline runtime / runtime mean`, using the same problem size at `nodes=1`, `ppn=1`, `threads=1`, and `total_cores=1` as baseline.",
        "",
        "`efficiency = speedup / total cores`. Values above 100% are possible in measured data and should be explained rather than automatically capped.",
        "",
        "`CUs = nodes x runtime mean / 3600`, where 1 CU is 1 node for 1 hour.",
        "",
        "## Problem Size Overview",
        "",
    ]

    overview = (
        aggregated[["scale", "m", "n", "unknowns"]]
        .drop_duplicates()
        .sort_values("scale")
    )
    overview_rows = [
        [str(row.scale), fmt_int(row.m), fmt_int(row.n), fmt_int(row.unknowns)]
        for row in overview.itertuples(index=False)
    ]
    lines.extend(markdown_table(["scale", "m", "n", "unknowns"], overview_rows))

    headers = [
        "total cores",
        "nodes",
        "ppn",
        "MPI ranks",
        "threads",
        "unknowns/core",
        "runtime mean/s",
        "speedup",
        "efficiency",
        "CUs",
    ]
    table_labels = {
        "small": "Table 1a",
        "medium-small": "Table 1b",
        "medium": "Table 1c",
        "large": "Table 1d",
        "very-large": "Table 1e",
    }

    for scale in SCALE_ORDER:
        scale_data = aggregated[aggregated["scale"].astype(str) == scale]
        if scale_data.empty:
            continue
        first = scale_data.iloc[0]
        lines.extend(
            [
                "",
                f"## {table_labels[scale]}. Runtime scaling for {scale} problem size",
                "",
                f"scale: `{scale}`",
                "",
                f"problem size: {fmt_int(first['m'])} x {fmt_int(first['n'])}",
                "",
                f"unknowns: {fmt_int(first['unknowns'])}",
                "",
            ]
        )

        body = []
        for row in scale_data.itertuples(index=False):
            body.append(
                [
                    fmt_int(row.total_cores),
                    fmt_int(row.nodes),
                    fmt_int(row.ppn),
                    fmt_int(row.mpi_ranks),
                    fmt_int(row.threads),
                    fmt_int(round(float(row.unknowns_per_core))),
                    fmt_float(row.runtime_mean_s, 2),
                    fmt_float(row.speedup, 2),
                    fmt_percent(row.efficiency),
                    fmt_cu(row.cu),
                ]
            )
        lines.extend(markdown_table(headers, body))

    output_path.write_text("\n".join(lines) + "\n")


def write_exercise_answers(aggregated: pd.DataFrame, output_path) -> None:
    lines = [
        "# Exercise Helper Answers",
        "",
        "Use this file after trying `lessons/exercises.md` yourself.",
        "",
        "## Fastest vs Cheapest Configuration",
        "",
        "| scale | fastest total cores | fastest threads | fastest runtime/s | cheapest total cores | cheapest threads | cheapest CUs |",
        "| --- | ---: | ---: | ---: | ---: | ---: | ---: |",
    ]
    for scale in SCALE_ORDER:
        scale_data = aggregated[aggregated["scale"].astype(str) == scale]
        if scale_data.empty:
            continue
        fastest = scale_data.loc[scale_data["runtime_mean_s"].idxmin()]
        cheapest = scale_data.loc[scale_data["cu"].idxmin()]
        lines.append(
            f"| {scale} | {int(fastest['total_cores'])} | {int(fastest['threads'])} | "
            f"{fastest['runtime_mean_s']:.2f} | {int(cheapest['total_cores'])} | "
            f"{int(cheapest['threads'])} | {cheapest['cu']:.4f} |"
        )

    over_100 = aggregated[aggregated["efficiency"] > 1.0]
    lines.extend(
        [
            "",
            "## Efficiency Above 100%",
            "",
            f"Rows with efficiency above 100%: {len(over_100)}",
            "",
            "These rows can be useful discussion points about superlinear speedup, baseline effects, and measurement variability.",
            "",
        ]
    )
    output_path.write_text("\n".join(lines))


def main() -> None:
    output_dir = ensure_output_dir()
    raw = load_runtime_results()
    clean = clean_runtime_results_for_tables(raw)
    aggregated = aggregate_runtime_tables(clean)

    csv_path = output_dir / "runtime_scaling_tables_pandas.csv"
    markdown_path = output_dir / "runtime_scaling_tables_pandas.md"
    answers_path = output_dir / "runtime_scaling_table_exercise_answers.md"

    aggregated.to_csv(csv_path, index=False)
    write_markdown_tables(aggregated, markdown_path)
    write_exercise_answers(aggregated, answers_path)

    print(f"Raw rows: {len(raw)}")
    print(f"Clean completed rows: {len(clean)}")
    print(f"Aggregated table rows: {len(aggregated)}")
    print(f"Wrote: {csv_path}")
    print(f"Wrote: {markdown_path}")
    print(f"Wrote: {answers_path}")


if __name__ == "__main__":
    main()
